//Arithmetic Operations
let x = 10;
let y = 5;

// ADD: Sum or Total
let sum = x + y;
console.log("The sum is:", sum)

// SUBTRACT: difference '-'
let difference = x - y;
console.log("The difference is:", difference)


// MULTIPLY: product '*'
let product = x * y;
console.log("The product is:", product)

// DIVIDE: quotient '/'
let quotient = x / y;
console.log("The quotient is:", quotient)

// Remainder or modulo
let remainder = x % y;
console.log("The remainder is:", remainder);

let answer = 10 + 2 * 23 / 2;
console.log("The answer is", answer);

// Concatenation
let a = "Hello";
let b = "World!";
console.log(a + " " + b)

//Assignment expressions
let i = 40;
let j = 20;

// Assignment expressions '+='
i += j;
console.log(i);

// Assignment expressions '-='
i -= j;
console.log(i);

// Assignment expressions '*='
i *= j;
console.log(i);

// Assignment expressions '/='
i /= j;
console.log(i);
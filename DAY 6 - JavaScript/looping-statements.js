// Control Flow Structure
// Looping statement

// FOR LOOP
for (let i = 1; i <= 5; i++) {
    console.log("For loop count:", i);
}

// WHILE LOOP
let count = 1;
while (count <= 5) {
    console.log("While loop count:", count);
    count++;
}

// DO-WHILE LOOP
let a = 1;
do {
    console.log("Do-while count:", a);
    a++;
} while (a <= 5);
